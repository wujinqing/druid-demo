package com.jin.druid.demo.bean;

/**
 * @author wu.jinqing
 * @date 2018年09月03日
 */
public class Greeting {
    private final long id;
    private final String content;

    public Greeting(long id, String content) {
        this.id = id;
        this.content = content;
    }

    public long getId() {
        return id;
    }

    public String getContent() {
        return content;
    }
}
